// Complete the method that takes a boolean value and return a "Yes" string for true, or a "No" string for false.

const boolYesOrNo = (bool) => (bool ? "Yes" : "No");

console.log(boolYesOrNo("Dado"));
console.log(boolYesOrNo(true));
console.log(boolYesOrNo(54));
console.log(boolYesOrNo(0));
console.log(boolYesOrNo(-23));
console.log(boolYesOrNo(false));
